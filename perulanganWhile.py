"""
bentuk perulangan memakai while
"""

print('perulangan memakai while')

jumlah_buku = 10
print('Ibu berkata baca semua bukumu')

jumlah_buku_ygsdh_dibaca = 0
print(f'jumlah buku yang sudah dibaca :{jumlah_buku_ygsdh_dibaca}')

while jumlah_buku_ygsdh_dibaca < jumlah_buku:
      jumlah_buku_ygsdh_dibaca = jumlah_buku_ygsdh_dibaca + 1
      print(f'jumlah buku yang sudah dibaca:{jumlah_buku_ygsdh_dibaca}')

print(f'jumlah buku yang dibaca : {jumlah_buku_ygsdh_dibaca}')
